const ALLOWED_MIME_TYPES = [
  "image/jpeg", "image/png", "image/gif", "image/webp",
  "application/pdf",
  "application/msword",
  "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
  "application/vnd.ms-excel",
  "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
  "text/plain",
  "application/zip", "application/x-rar-compressed",
];

export function sanitizeFilename(name: string): string {
  let result = "";
  for (const ch of name) {
    result += CYRILLIC_MAP[ch] || ch;
  }
  result = result.replace(/[<>:"\/\\|?*\x00-\x1f]/g, "_");
  result = result.replace(/\s+/g, " ").trim();
  if (result.length > 200) {
    const ext = path.extname(result);
    result = result.slice(0, 200 - ext.length) + ext;
  }
  return result || "unnamed";
}

export function isAllowedMimeType(mimeType: string): boolean {
  return ALLOWED_MIME_TYPES.includes(mimeType);
}

export function formatFileSize(bytes: number): string {
  if (bytes < 1024) return bytes + " B";
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + " KB";
  return (bytes / (1024 * 1024)).toFixed(1) + " MB";
}

const PDF_HEADER = Buffer.from([0x25, 0x50, 0x44, 0x46]);
const JPEG_HEADER = Buffer.from([0xFF, 0xD8, 0xFF]);
const PNG_HEADER = Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A]);
const GIF87_HEADER = Buffer.from([0x47, 0x49, 0x46, 0x38, 0x37, 0x61]);
const GIF89_HEADER = Buffer.from([0x47, 0x49, 0x46, 0x38, 0x39, 0x61]);
const ZIP_HEADER = Buffer.from([0x50, 0x4B, 0x03, 0x04]);

export function validateFileContent(filePath: string, declaredMime: string): boolean {
  if (!fs.existsSync(filePath)) return false;
  const buf = Buffer.alloc(16);
  const fd = fs.openSync(filePath, "r");
  fs.readSync(fd, buf, 0, 16, 0);
  fs.closeSync(fd);

  switch (declaredMime) {
    case "image/jpeg":
      return buf.slice(0, 3).equals(JPEG_HEADER);
    case "image/png":
      return buf.slice(0, 8).equals(PNG_HEADER);
    case "image/gif":
      return buf.slice(0, 6).equals(GIF87_HEADER) || buf.slice(0, 6).equals(GIF89_HEADER);
    case "image/webp":
      return buf.slice(0, 4).equals(Buffer.from([0x52, 0x49, 0x46, 0x46]));
    case "application/pdf":
      return buf.slice(0, 4).equals(PDF_HEADER);
    case "application/zip":
    case "application/x-rar-compressed":
      return buf.slice(0, 4).equals(ZIP_HEADER);
    default:
      return true;
  }
}

export function getContentDisposition(mimeType: string): "inline" | "attachment" {
  if (mimeType.startsWith("image/")) return "inline";
  if (mimeType === "application/pdf") return "inline";
  if (mimeType === "text/plain") return "inline";
  return "attachment";
}

