export const FILE_LIMITS = {
  maxSizeBytes: 50 * 1024 * 1024,
  maxFilesPerEntity: 20,
  defaultPageSize: 20,
  maxPageSize: 100,
  maxFileNameLength: 255,
  maxOriginalNameLength: 500,
  allowedMimeTypes: [
    "image/jpeg", "image/png", "image/gif", "image/webp",
    "application/pdf",
    "application/msword",
    "application/vnd.openxmlformats-officedocument.wordprocessingml.document",
    "application/vnd.ms-excel",
    "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet",
    "text/plain",
    "application/zip", "application/x-rar-compressed",
  ] as readonly string[],
  unsafePreviewTypes: [
    "text/html", "text/javascript", "application/javascript",
    "application/x-javascript", "image/svg+xml", "application/xml",
    "text/xml", "application/x-httpd-php", "text/ecmascript",
    "application/ecmascript", "application/json",
  ] as readonly string[],
  storageConfig: {
    provider: process.env.STORAGE_PROVIDER || "local",
    uuidVersion: "v4",
  },
};

export function sanitizeFilename(name: string): string {
  if (!name || typeof name !== "string") return "unnamed";
  if (name.includes("\0")) throw Object.assign(new Error("Filename contains null byte"), { statusCode: 400 });
  if (Buffer.byteLength(name, "utf-8") > FILE_LIMITS.maxOriginalNameLength) {
    name = Buffer.from(name).slice(0, FILE_LIMITS.maxOriginalNameLength).toString("utf-8").replace(/\0/g, "");
  }
  let result = "";
  for (const ch of name) {
    result += CYRILLIC_MAP[ch] || ch;
  }
  result = result.replace(/[<>:"\/\\|?*\x00-\x1f]/g, "_");
  result = result.replace(/\s+/g, " ").trim();
  if (result.length > FILE_LIMITS.maxFileNameLength) {
    const ext = path.extname(result);
    const base = result.slice(0, FILE_LIMITS.maxFileNameLength - ext.length);
    result = base + ext;
  }
  return result || "unnamed";
}

export function generateStorageName(ext: string): string {
  const safeExt = ext.replace(/[^a-zA-Z0-9.]/g, "").slice(0, 10) || "";
  return crypto.randomUUID() + (safeExt ? "." + safeExt.replace(/^\.?/, "") : "");
}

export function isAllowedMimeType(mimeType: string): boolean {
  return FILE_LIMITS.allowedMimeTypes.includes(mimeType as any);
}

export function isUnsafePreviewType(mimeType: string): boolean {
  return FILE_LIMITS.unsafePreviewTypes.includes(mimeType as any);
}

export function formatFileSize(bytes: number): string {
  if (bytes < 1024) return bytes + " B";
  if (bytes < 1024 * 1024) return (bytes / 1024).toFixed(1) + " KB";
  return (bytes / (1024 * 1024)).toFixed(1) + " MB";
}

