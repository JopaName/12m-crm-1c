export function validateFileContent(filePath: string, declaredMime: string): boolean {
  if (!fs.existsSync(filePath)) return false;
  var fd: number | null = null;
  try {
    var buf = Buffer.alloc(16);
    fd = fs.openSync(filePath, "r");
    fs.readSync(fd, buf, 0, 16, 0);

    switch (declaredMime) {
      case "image/jpeg":
        return buf.slice(0, 3).equals(Buffer.from([0xFF, 0xD8, 0xFF]));
      case "image/png":
        return buf.slice(0, 8).equals(Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A]));
      case "image/gif":
        return buf.slice(0, 6).equals(Buffer.from([0x47, 0x49, 0x46, 0x38, 0x37, 0x61]))
          || buf.slice(0, 6).equals(Buffer.from([0x47, 0x49, 0x46, 0x38, 0x39, 0x61]));
      case "image/webp":
        return buf.slice(0, 4).equals(Buffer.from([0x52, 0x49, 0x46, 0x46]));
      case "application/pdf":
        return buf.slice(0, 4).equals(Buffer.from([0x25, 0x50, 0x44, 0x46]));
      case "application/msword":
      case "application/vnd.openxmlformats-officedocument.wordprocessingml.document":
      case "application/vnd.ms-excel":
      case "application/vnd.openxmlformats-officedocument.spreadsheetml.sheet":
      case "application/zip":
      case "application/x-rar-compressed":
        return buf.slice(0, 4).equals(Buffer.from([0x50, 0x4B, 0x03, 0x04]));
      default:
        return true;
    }
  } catch (e) {
    return false;
  } finally {
    if (fd !== null) {
      try { fs.closeSync(fd); } catch { /* ignore */ }
    }
  }
}

export function getContentDisposition(mimeType: string, fileName: string): string {
  var disposition: "inline" | "attachment";
  if (mimeType.startsWith("image/")) disposition = "inline";
  else if (mimeType === "application/pdf") disposition = "inline";
  else if (mimeType === "text/plain") disposition = "inline";
  else disposition = "attachment";

  var safeName = fileName.replace(/[\r\n"]/g, "_").replace(/\\/g, "/");
  var encodedName = encodeURIComponent(safeName).replace(/%20/g, " ");

  return disposition
    + `; filename="${safeName.replace(/[^ -~]/g, "?")}"`
    + `; filename*=UTF-8''${encodedName}`;
}

